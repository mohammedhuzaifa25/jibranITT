function showSuggestions() {
    let input = document.getElementById('autocomplete-input').value;
    let listContainer = document.getElementById('autocomplete-list');
    listContainer.innerHTML = ''; // Clear previous suggestions

    if (!input) {
        return; // If input is empty, don't show suggestions
    }

    // Filter countries that start with the input value
    let filteredCountries = countries.filter(country =>
        country.toLowerCase().startsWith(input.toLowerCase())
    );

    // Create the dropdown list for filtered countries
    filteredCountries.forEach(country => {
        let item = document.createElement('div');
        item.className = 'autocomplete-item';
        item.innerText = country;

        // Add click event to populate the input when an item is clicked
        item.onclick = function () {
            document.getElementById('autocomplete-input').value = country;
            listContainer.innerHTML = ''; // Clear suggestions after selection
        };

        listContainer.appendChild(item);
    });
}
document.addEventListener('click', function (e) {
    if (!e.target.matches('#autocomplete-input')) {
        document.getElementById('autocomplete-list').innerHTML = '';
    }
});
